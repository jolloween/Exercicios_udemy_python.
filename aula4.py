# Tipos int e float
# int -> Número inteiro
# O tipo inteiro representa qualquer número
# Positivo ou negativo. int sem sinal é considerado positivo.
print(11) # int
print(-11) # int
print(0) # int
#Float -> Número com pomto flutuante
# O tipo float representa qualquer número
# positivo ou negativo com ponto flutuante.
# float sem sinal é considerado positivo.
print(1.1, 10.11 ) # float
print(0.0, -1.5)# float

# A função type mostra o tipo que o python 
# inferiu ao valor.
print(type(5.5))
print(type(5))
print(type(-5.5), type(-11), type(0.0))
print(type('João Paulo'))




